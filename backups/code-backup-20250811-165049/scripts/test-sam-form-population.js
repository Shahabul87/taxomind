#!/usr/bin/env node

/**
 * Test script to verify SAM's form population capability
 * Run this in the browser console on a course edit page
 */

// Test 1: Check if form metadata is present
console.log('🔍 Test 1: Checking for form metadata...');
const formMetadata = document.querySelector('[data-sam-form-metadata="course-description"]');
if (formMetadata) {
  console.log('✅ Form metadata found:', {
    formId: formMetadata.getAttribute('data-form-id'),
    purpose: formMetadata.getAttribute('data-form-purpose'),
    alternateId: formMetadata.getAttribute('data-form-alternate-id'),
    isEditing: formMetadata.getAttribute('data-is-editing'),
    currentValue: formMetadata.getAttribute('data-current-value')?.substring(0, 50) + '...'
  });
} else {
  console.log('❌ Form metadata not found');
}

// Test 2: Dispatch SAM populate event
console.log('\n🔍 Test 2: Dispatching SAM populate event...');
const testDescription = '<p>This is a test description generated by SAM. It includes <strong>bold text</strong> and <em>italic text</em> to test rich text formatting.</p>';

// Listen for success event
window.addEventListener('sam-form-populated', (event) => {
  console.log('✅ Received form populated event:', event.detail);
}, { once: true });

// Dispatch the populate event
window.dispatchEvent(new CustomEvent('sam-populate-form', {
  detail: {
    formId: 'course-description-form',
    data: {
      description: testDescription
    }
  }
}));

console.log('📤 Event dispatched. Check if form opened and was populated.');

// Test 3: After a delay, check if form is visible
setTimeout(() => {
  console.log('\n🔍 Test 3: Checking form state after event...');
  
  const form = document.getElementById('course-description-form');
  if (form) {
    console.log('✅ Form is now visible');
    
    // Check TipTap editor
    const proseMirror = form.querySelector('.ProseMirror');
    if (proseMirror) {
      console.log('✅ TipTap editor found');
      console.log('📝 Editor content:', proseMirror.innerHTML.substring(0, 100) + '...');
    } else {
      console.log('❌ TipTap editor not found');
    }
  } else {
    console.log('❌ Form not found or not visible');
  }
  
  // Check if edit mode was activated
  const editButton = document.querySelector('button:has(svg.lucide-pencil)');
  const cancelButton = Array.from(document.querySelectorAll('button')).find(btn => btn.textContent === 'Cancel');
  if (cancelButton) {
    console.log('✅ Edit mode is active (Cancel button visible)');
  } else {
    console.log('❌ Edit mode not active');
  }
}, 1000);

// Test 4: Test alternate form IDs
console.log('\n🔍 Test 4: Testing alternate form IDs...');
const alternateIds = ['update-description', 'general-form', 'course-description'];
alternateIds.forEach(id => {
  setTimeout(() => {
    console.log(`\n📤 Testing with form ID: ${id}`);
    window.dispatchEvent(new CustomEvent('sam-populate-form', {
      detail: {
        formId: id,
        data: {
          description: testDescription + ` (via ${id})`
        }
      }
    }));
  }, 2000);
});

console.log('\n📋 Test script completed. Check console output above for results.');